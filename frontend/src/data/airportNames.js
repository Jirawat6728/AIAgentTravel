/**
 * คืน "IATA (ชื่อเต็ม)" หรือ "IATA" ถ้าไม่มีชื่อ
 */
export function getAirportDisplay(code) {
  if (!code) return '';
  const c = String(code).toUpperCase();
  const name = AIRPORT_NAMES[c];
  return name ? `${c} (${name})` : c;
}
